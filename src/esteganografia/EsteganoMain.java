    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if(!imagem){
            JOptionPane.showMessageDialog(null, "Selecione uma imagem!");
        }else        
        if(!(
                (jTextArea1.getText().equals("")) || 
                (jTextArea1.getText().equals("Digite algo...")) )){
       
            Imagem a = new Imagem();
            Texto t = new Texto();
            
            String texto=jTextArea1.getText();
            String textoBinario="";
            
            //Converte o texto que escrevemos para bits
            textoBinario=t.bitsTexto(texto);
            
            try { 
                
                //vai fazer toda a operacao da imagem: abrir, mudar e desenhar a imagem de novo
                a.abrirImagem(url,textoBinario);
                
                
            } catch (IOException ex) {
                System.out.println("ERRO ABRIR = "+ex);
            }
            
        }
        else
            JOptionPane.showMessageDialog(null,"Digite algum texto!");
    }//GEN-LAST:event_jButton2ActionPerformed

