    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        JFileChooser jfc = new JFileChooser();
        
        jfc.setFileFilter(new FileNameExtensionFilter("Imagem", "jpg", "jpeg", "gif"));
        jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //jfc.showDialog(jfc, "Selecione");
       int resultado = jfc.showOpenDialog(this);  
  
        if ( resultado == JFileChooser.CANCEL_OPTION )  
            JOptionPane.showMessageDialog(null,"Nenhuma Imagem foi selecionada!");
        else{
        url=String.valueOf(jfc.getSelectedFile().toURI());
        jLabel4.setText(jfc.getSelectedFile().getName());
        imagem=true;

        BufferedImage imagemCapturada;
            imagemCapturada = null;
            try {
                imagemCapturada = ImageIO.read(new File(jfc.getSelectedFile().getPath()));
            } catch (IOException ex) {
                Logger.getLogger(EsteganoMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            Icon novaImg;
            novaImg = new ImageIcon(imagemCapturada.getScaledInstance(jLabel3.getWidth(),jLabel3.getHeight(), imagemCapturada.SCALE_DEFAULT));
            jLabel3.setIcon(novaImg);
            
       } 
    }//GEN-LAST:event_jButton1ActionPerformed

